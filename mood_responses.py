def mood_response(mood):
    if mood == "happy":
        return "Right on!"
    elif mood == "angry":
        return "Whoosah"
    elif mood == "sad":
        return "It's okay to cry"
    elif mood == "excited":
        return "Weeeeeeeee!"
    elif mood == "serene":
        return "Om Mani Padme Hum"